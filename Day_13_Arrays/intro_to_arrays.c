#include <stdio.h>

int main() {
  // Declaring an array.
  int marks[3] = {85, 92, 78};  // 3 elements.

  // Accessing elements of an array.
  printf("Marks of student 1: %d\n", marks[0]);
  printf("Marks of student 2: %d\n", marks[1]);
  printf("Marks of student 3: %d\n", marks[2]);
  return 0;
}
