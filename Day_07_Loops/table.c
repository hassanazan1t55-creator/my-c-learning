#include <stdio.h>

int main() {
    int counting;

    // loop run 10 times for table
    // (Table ke liye loop 10 dafa chalega)
    for(counting = 1; counting <= 10; counting++) {
        
        // print table design and do math
        // (Table ka design banana aur math karna)
        printf("2 x %d = %d\n", counting, 2 * counting);
    }

    return 0;
}
